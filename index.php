        <h3>J-hope BTS Berikan Donasi 200 Juta Won untuk Penelitian Penyakit Langka Anak-anak</h3>
        <p>J-hope BTS merayakan ulang tahunnya dengan memberikan kembali cinta yang telah diterimanya dengan cara yang bermakna. Pada tanggal 18 Februari, untuk merayakan ulang tahunnya, j-hope menyumbangkan 200 juta won (sekitar $138.508) ke Asan Medical Center di Seoul.

            <br/><br/>Dana tersebut nantinya akan meningkatkan fasilitas dan perawatan medis untuk pasien anak-anak dan remaja serta mendukung penelitian tentang penyakit anak-anak yang langka dan serius.
            
            <br/><br/>J-hope mengungkapkan pemikirannya tentang sumbangan tersebut dengan harapakan agar donasinya tersebut membantu anak-anak yang sedang menderita.
            
            <br/><br/>"Saya harap sumbangan ini dapat memberikan sedikit bantuan kepada anak-anak yang sedang menderita saat ini. Saya dengan tulus berharap mereka memiliki masa depan yang sehat dan penuh harapan," ungkap J-hope.
            
            <br/><br/>Anggota BTS tersebut juga berbagi keinginannya untuk terus mendukung tujuan tersebut."Saya ingin memberikan kembali cinta yang selalu ditunjukkan ARMY kepada saya. Dimulai dengan ulang tahun ini, saya merasa terhormat untuk terus berdonasi setiap tahun," lanjutnya.
            
            <br/><br/>J-hope secara konsisten menunjukkan komitmennya untuk memberi kembali. Kontribusinya baru-baru ini termasuk 100 juta won (sekitar $69.224) untuk mendukung korban tragedi kecelakaan pesawat pada bulan Januari, 100 juta won untuk bantuan gempa bumi Turki-Suriah pada tahun 2023, 100 juta won untuk Seoul Asan Medical Center pada tahun 2022, dan 100 juta won lainnya untuk mendukung anak-anak kurang mampu pada tahun 2021.
